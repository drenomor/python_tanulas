#!/usr/bin/env python3

# while ciklus - while loop

ciklus_valtozo = 0

# Amíg a feltétel igaz addig hajtja végre a ciklus magot. 
# Amíg ciklus_valozo kisebb mint 5 addig írd ki hogy Hello 
# növeld a ciklus_valtozo erteket 1-el.
while ciklus_valtozo < 5:
    print("Hello")
    ciklus_valtozo += 1
