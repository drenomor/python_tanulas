#!/usr/bin/env python3

# Aritmetikai operátorok
# +, -, *, /, **, //, %

szam1 = 3
szam2 = 21

print(szam1 + szam2)    # összeadás
print(szam1 - szam2)    # kivonás
print(szam1 * szam2)    # szorzás
print(szam1 / szam2)    # osztás
print(szam2 / szam1)
print(szam1 ** szam2)   # hatványozás
print(szam1 // szam2)   # egész számos osztás
print(szam1 % szam2)    # maradékos osztás
