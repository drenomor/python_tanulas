#!/usr/bin/env python3

# Értékadó operátorok - assignment operators

# =, +=, -=, *=, /=, **=, //=, %=

number = 12
print(number)

# number = number + 1

number += 1
print(number)

# number = number - 1

number -= 1
print(number)

# number = number * 2

number *= 2
print(number)

# number = number / 3

number /= 3
print(number)

# number = number ** 2

number **= 2
print(number)

# number = number // 7

number //= 7
print(number)

number = 64

# number = number % 3

number %= 3
print(number)
